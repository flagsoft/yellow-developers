                    $outputData .= "    background-image: url(\"{$cdn}svg/$image.svg\");\n";
                    $outputData .= "}\n";
                }
                $output .= "<style type=\"text/css\">\n$outputData</style>\n";
            }
        }
        return $output;
    }
    
    // Normalise emoji CSS class
    public function normaliseClass($text) {
        return strreplaceu(array("+1", "-1", "_"), array("plus1", "minus1", "-"), $text);
    }
    
    // Normalise text with emoji, convert UTF8 and shortcode
    public function normaliseText($text, $convertUtf8 = true, $convertShortcode = true) {
        if ($convertUtf8) {
            $lookup = array();
            foreach ($this->getLookupData() as $entry) {
                $lookup[$entry["utf8"]] = ":".$entry["shortname"].":";
            }
            krsort($lookup);
            $text = str_replace(array_keys($lookup), array_values($lookup), $text);
        }
        if ($convertShortcode) {
            $thisCompatible = $this;
            $callback = function ($matches) use ($thisCompatible) {
                $output = $thisCompatible->onParseContentShortcut(null, "", $matches[1], "symbol");
                if (is_null($output)) $output = $matches[0];
                return $output;
            };
            $text = preg_replace_callback("/\:([\w\+\-\_]+)\:/", $callback, $text);
        }
        return $text;
    }
    
    // Check if emoji shortname exists
    public function isShortname($shortname) {
        $found = false;
        foreach ($this->getLookupData() as $entry) {
            if ($entry["shortname"]==$shortname) {
                $found = true;
                break;
            }
        }
        return $found;
    }
    
